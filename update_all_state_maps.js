const mapsDir = path.join(__dirname, 'src', 'maps');
states.forEach((state) => {
  const file = path.join(mapsDir, `${state.name.toLowerCase().replace(/ /g, '-')}-map.html`);
  if (!fs.existsSync(file)) return;
  let html = fs.readFileSync(file, 'utf8');
  // Replace any d3.json path with the correct /data/{state}.geojson
  const geoPath = `/data/${state.name.toLowerCase().replace(/ /g, '-')}.geojson`;
  html = html.replace(/d3\.json\(["'][^"']+["']\)/, `d3.json("${geoPath}")`);
  fs.writeFileSync(file, html);
});
console.log('All state map HTML files updated to reference their correct GeoJSON.');
